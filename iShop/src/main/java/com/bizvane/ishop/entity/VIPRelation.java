package com.bizvane.ishop.entity;

public class VIPRelation {

	private int id;

	private String open_id;

	private String app_user_name;

	private String guider_code;

	private String create_date;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getOpen_id() {
		return open_id;
	}

	public void setOpen_id(String open_id) {
		this.open_id = open_id;
	}

	public String getApp_user_name() {
		return app_user_name;
	}

	public void setApp_user_name(String app_user_name) {
		this.app_user_name = app_user_name;
	}

	public String getGuider_code() {
		return guider_code;
	}

	public void setGuider_code(String guider_code) {
		this.guider_code = guider_code;
	}

	public String getCreate_date() {
		return create_date;
	}

	public void setCreate_date(String create_date) {
		this.create_date = create_date;
	}

	
}
